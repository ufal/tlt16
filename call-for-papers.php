<h2>Motivation and aims</h2>

<p>Treebanks have proved to be crucial resources for very important NLP applications, such as machine translation and information extraction, as well as supporting resources for various NLP tasks, such as high-quality parsing and POS tagging. 
</p>

<p>More recent trends in treebank-related research have focused on a variety of initiatives, such as annotating deep syntactic, semantic, and syntactic-semantic interface information; (semi)-automatic conversion of existing treebanks into deeper linguistic formats; multilingual and cross-lingual treebanking, including 'language-universal' treebanking; typology and its relation to treebanking; enriching treebanks with additional layers of linguistic annotation as well as incorporating world knowledge; dynamic treebanking involving a close connection between grammar-based parsing and manual annotation; designing web services for observing and exploiting theoretically diverse treebanks; and mapping syntactic and semantic knowledge to Linked Open Data (LOD) information. 
</p>

