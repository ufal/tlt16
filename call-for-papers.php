<p>TLT serves as a venue for new and ongoing research on the topic of linguistics and treebanks. The 16th edition of TLT will, for the second time, take place in Prague, Czech Republic, at Charles University in the heart of the old city, on 23-24 January 2018.
</p>

<p>For over 15 years now, TLT has served as a venue for new and ongoing high-quality work related to syntactically-annotated corpora, i.e., treebanks; with a focus on all the aspects of treebanking – descriptive, theoretical, formal and computational – but also going beyond treebanks, including other levels of annotation such as frame semantics and similar formalisms, coreference, discourse, named entities or events, to name only a few. 
</p>

<p>Submissions are invited for papers, posters and demonstrations which present research on treebanks and their intersection with linguistics, natural language processing and related fields. 
</p>

<p>
The TLT16 conference is collocated with a <a href="https://typo.uni-konstanz.de/dataprovenance/">Workshop on Provenance and Annotation in Computational Linguistics 2018</a> (Jan. 22, organized by Miriam Butt of Konstanz Univ., Germany) 
and immediately followed by the <a href="https://www.oeaw.ac.at/ac/crh2/">CDH workshop</a>, which takes place 
in Vienna, Austria on January 25-26, 2018.
</p>
	
