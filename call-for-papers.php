<h2>Special topic</h2>

<p>This year, we specifically invite papers on all aspects of the relation of linguistically motivated complex annotation, such as treebanking, and deep learning methods. Topics of such papers may include, but are not limited to:</p>
<ul>
	<li>a comparison of application performance using a linguistic representation vs. end-to-end Deep Neural Network system, or any particular aspects of it (e.g., differences in recall vs. precision)</li>
	<li>analysis of errors made by a deep-learning system vs. a system using linguistic features or representation</li>
	<li>approaches to "soften" categorical annotation usually present in treebanks by using distributional methods</li>
	<li>specific aspects of deep learning when applied to complex treebanks</li>
	<li>advantages or disadvantages of deep learning from treebanks in a multilingual setting.  </li>
</ul>

<h2>Instructions for authors</h2>

<p>The program for TLT16 will consist of full papers, posters and software demonstrations. 
	This year, we ask authors <b>not</b> to anonymize their submissions; 
we believe that full anonymity is difficult to achieve, since many papers now appear as preprints on the web and it is easy to find authors; 
	for demonstrations, it is almost impossible in any case due to the use of system names, references to previous versions etc. 
	Submissions must be written in English, and conform to the TLT16 style files available at
<a href="http://ufal.mff.cuni.cz/tlt16/style-tlt16.zip">http://ufal.mff.cuni.cz/tlt16/style-tlt16.zip</a>.</p>

<p>All submissions should be full-length papers of up to 7 pages plus unlimited references. 
It is assumed that system demonstration papers might be shorter; 
	in any case, use an appropriate length given what is the idea you are conveying in your contribution and the amount of technical details and/or examples you want to put in. 
	Accepted final papers will get an extra page (8 pages total) plus unlimited number of pages for references 
	and up to 4 pages for an optional appendix which could serve to publish algorithms, code snippets 
	and/or additional examples.</p>

<p>The organizers will work with the ACL Anthology to list the TLT proceedings there. 
	Authors of outstanding papers as identified by the PC members will be invited to submit an extended version 
	to The Prague Bulletin of Mathematical Linguistics (<a href="https://ufal.mff.cuni.cz/pbml">https://ufal.mff.cuni.cz/pbml</a>), 
	an ERIH-Plus listed journal published online by deGruyter in cooperation with Charles University.</p>

<p>Please upload your submissions via EasyChair: <a href="https://easychair.org/conferences/?conf=tlt16">https://easychair.org/conferences/?conf=tlt16</a></p>

