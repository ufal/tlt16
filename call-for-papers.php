<p>TLT serves as a venue for new and ongoing research on the topic of linguistics and treebanks. The 16th edition of TLT will, for the second time, take place in Prague, Czech Republic, at Charles University in the heart of the old city, on 23-24 January 2018.</p>

<p>For over 15 years now, TLT has served as a venue for new and ongoing high-quality work related to syntactically-annotated corpora, i.e., treebanks; with a focus on all the aspects of treebanking – descriptive, theoretical, formal and computational – but also going beyond treebanks, including other levels of annotation such as frame semantics and similar formalisms, coreference, discourse, named entities or events, to name only a few.</p>

<p>Submissions are invited for papers, posters and demonstrations which present research on treebanks and their intersection with linguistics, natural language processing and other related fields.</p>

<p>TLT16 website: <a href="http://ufal.mff.cuni.cz/tlt16" title="TLT16 website">http://ufal.mff.cuni.cz/tlt16</a></p>

