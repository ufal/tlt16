  <div id="node-ufal-event-full-group-toolbox" class="group_highlight_box group-toolbox field-group-div"><div class="field field-name-field-date-range field-type-datetime field-label-hidden"><div class="field-items"><div class="field-item even"><span class="date-display-single">Monday, January 1, 2018 - 00:00</span></div></div></div></div><div class="field field-name-title field-type-ds field-label-hidden"><div class="field-items"><div class="field-item even"><h2>Sixteenth International Workshop on Treebanks and Linguistic Theories</h2></div></div></div><div class="field field-name-body field-type-text-with-summary field-label-hidden"><div class="field-items"><div class="field-item even"><h4 style="box-sizing: border-box; font-family: inherit; font-weight: 500; line-height: 1.1; color: inherit; margin-top: 10px; margin-bottom: 10px; font-size: 18px;">Prague, January, 2018</h4>

<div id="tlt15" style="box-sizing: border-box; color: rgb(51, 51, 51); font-family: &quot;Helvetica Neue&quot;, Helvetica, Arial, sans-serif; font-size: 14px;">
<h4 style="box-sizing: border-box; font-family: inherit; font-weight: 500; line-height: 1.1; color: inherit; margin-top: 10px; margin-bottom: 10px; font-size: 18px;">Overview</h4>
<img alt="" src="/~zeman/2017/images/12486059_549497891884167_132859978721883586_o.jpg" style="height: 375px; width: 300px; float: right;" /><p style="box-sizing: border-box; margin: 0px 0px 10px;">TLT serves as a venue for new and ongoing research on the topic of linguistics and treebanks. The 16th edition of TLT will return to the heart of Europe, at Charles University, Prague, in January 2018.</p>

<p style="box-sizing: border-box; margin: 0px 0px 10px;">For 16 years now, TLT has served as a venue for new and ongoing high-quality work related to syntactically-annotated corpora, i.e., treebanks; with a focus on all the aspects of treebanking – descriptive, theoretical, formal and computational – but also going beyond treebanks, including other levels of annotation such as frame semantics, coreference or events, to name only a few.</p>

<p style="box-sizing: border-box; margin: 0px 0px 10px;">Local chair: <a href="/jan-hajic">Jan Hajič</a></p>
